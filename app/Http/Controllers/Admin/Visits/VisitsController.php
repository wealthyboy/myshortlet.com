     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $visits = \DB::table('user_trackings')
            ->whereIn('id', function ($query) {
                $query->selectRaw('MIN(id)')
                    ->from('user_trackings')
                    ->groupBy('session_id');
            })
            ->orderByDesc('id')
            ->paginate(20);

        return view('admin.visits.index', compact('visits'));
    }

